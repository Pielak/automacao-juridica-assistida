//! Tauri IPC commands for local AI chat via llama.cpp/Ollama.
//!
//! Provides streaming chat functionality through Tauri's event system.
//! All user messages are pseudonymized before being sent to the LLM,
//! and responses are de-pseudonymized before being returned to the frontend.

use serde::{Deserialize, Serialize};
use tauri::{command, AppHandle, Manager, Runtime};
use tokio::sync::Mutex;
use std::sync::Arc;

use crate::domain::entities::{SigiloLevel, ChatMessage, ChatRole};
use crate::domain::errors::AppError;
use crate::infra::sidecar::SidecarManager;

