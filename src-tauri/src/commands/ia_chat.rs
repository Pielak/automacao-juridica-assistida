/// Default system prompt for legal assistant context.
const DEFAULT_SYSTEM_PROMPT: &str = r#"Você é um assistente jurídico especializado no direito brasileiro. 
Responda de forma precisa, citando legislação, jurisprudência e doutrina quando pertinente. 
Nunca invente informações. Se não souber, diga que não sabe. 
Todas as informações pessoais foram substituídas por placeholders — não tente adivinhar dados reais."#;

/// Build the messages payload to send to the Python sidecar.
fn build_sidecar_payload(
    request: &ChatRequest,
) -> serde_json::Value {
    let system_prompt = request
        .system_prompt
        .as_deref()
        .unwrap_or(DEFAULT_SYSTEM_PROMPT);

    let mut messages = Vec::with_capacity(request.history.len() + 2);

    // System message
    messages.push(serde_json::json!({
        "role": "system",
        "content": system_prompt
    }));

    // History
    for msg in &request.history {
        messages.push(serde_json::json!({
            "role": msg.role,
            "content": msg.content
        }));
    }

    // Current user message
    messages.push(serde_json::json!({
        "role": "user",
        "content": request.message
    }));

    let mut payload = serde_json::json!({
        "conversation_id": request.conversation_id,
        "messages": messages,
        "stream": true,
    });

    if let Some(ref model) = request.model {
        payload["model"] = serde_json::json!(model);
    }
    if let Some(temp) = request.temperature {
        payload["temperature"] = serde_json::json!(temp);
    }
    if let Some(max) = request.max_tokens {
        payload["max_tokens"] = serde_json::json!(max);
    }

    payload
}

