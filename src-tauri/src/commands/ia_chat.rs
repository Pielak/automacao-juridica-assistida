/// Payload sent from the frontend to start a chat completion.
#[derive(Debug, Clone, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct ChatRequest {
    /// Unique conversation identifier (UUIDv4 generated by the frontend).
    pub conversation_id: String,
    /// The user's message content (plain text, may contain PII).
    pub message: String,
    /// Previous messages in the conversation for context.
    #[serde(default)]
    pub history: Vec<ChatMessageDto>,
    /// Sigilo (confidentiality) level of the current processo context.
    #[serde(default = "default_sigilo")]
    pub sigilo_level: u8,
    /// Optional: model override (e.g. "llama3", "mistral").
    pub model: Option<String>,
    /// Optional: system prompt override for specialised legal tasks.
    pub system_prompt: Option<String>,
    /// Optional: temperature override (0.0 – 1.0).
    pub temperature: Option<f64>,
    /// Optional: max tokens for the response.
    pub max_tokens: Option<u32>,
}

fn default_sigilo() -> u8 {
    1
}

/// DTO for a single chat message exchanged over IPC.
#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct ChatMessageDto {
    pub role: String,   // "system" | "user" | "assistant"
    pub content: String,
}

/// Streamed chunk emitted to the frontend via Tauri events.
#[derive(Debug, Clone, Serialize)]
#[serde(rename_all = "camelCase")]
pub struct ChatStreamChunk {
    pub conversation_id: String,
    /// Incremental text delta.
    pub delta: String,
    /// Whether this is the final chunk.
    pub done: bool,
    /// Cumulative token count (if available from the backend).
    pub token_count: Option<u32>,
}

/// Final response returned by the non-streaming command variant.
#[derive(Debug, Clone, Serialize)]
#[serde(rename_all = "camelCase")]
pub struct ChatResponse {
    pub conversation_id: String,
    pub content: String,
    pub model: String,
    pub token_count: u32,
}

/// Lightweight status response.
#[derive(Debug, Clone, Serialize)]
#[serde(rename_all = "camelCase")]
pub struct AiStatusResponse {
    pub available: bool,
    pub backend: String, // "ollama" | "llama_cpp" | "none"
    pub models: Vec<String>,
}

