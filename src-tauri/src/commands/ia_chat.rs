/// Stream a chat completion from the local LLM.
///
/// The command:
/// 1. Sends the user message to the Python sidecar for pseudonymization.
/// 2. Forwards the pseudonymized prompt to the local LLM (Ollama / llama.cpp).
/// 3. Streams response chunks back to the frontend via Tauri events (`ia:chat-stream`).
/// 4. De-pseudonymizes the final response before the last chunk.
///
/// # Events emitted
/// - `ia:chat-stream` — `ChatStreamChunk` for each token / group of tokens.
/// - `ia:chat-error`  — `{ conversationId, error }` on failure.
#[command]
pub async fn ia_chat_stream<R: Runtime>(
    app: AppHandle<R>,
    request: ChatRequest,
) -> Result<(), String> {
    // Validate sigilo level
    if request.sigilo_level > 5 {
        return Err("Nível de sigilo inválido (deve ser 1-5)".into());
    }

    let conversation_id = request.conversation_id.clone();

    // Spawn the streaming task so the command returns immediately.
    let app_handle = app.clone();
    tokio::spawn(async move {
        if let Err(e) = run_chat_stream(&app_handle, &request).await {
            let _ = app_handle.emit(
                "ia:chat-error",
                serde_json::json!({
                    "conversationId": conversation_id,
                    "error": e.to_string()
                }),
            );
        }
    });

    Ok(())
}

/// Internal implementation of the streaming chat pipeline.
async fn run_chat_stream<R: Runtime>(
    app: &AppHandle<R>,
    request: &ChatRequest,
) -> Result<(), AppError> {
    let sidecar = app
        .try_state::<Arc<Mutex<SidecarManager>>>()
        .ok_or_else(|| AppError::Internal("Sidecar manager not initialised".into()))?;

    let sidecar_guard = sidecar.lock().await;

    // -----------------------------------------------------------------------
    // Step 1: Pseudonymize the user message via the Python sidecar
    // -----------------------------------------------------------------------
    let pseudonymize_payload = serde_json::json!({
        "action": "pseudonymize",
        "text": request.message,
        "sigilo_level": request.sigilo_level,
    });

    let pseudo_response = sidecar_guard
        .call("pseudonymize", pseudonymize_payload)
        .await
        .map_err(|e| AppError::Sidecar(format!("Pseudonymization failed: {e}")))?;

    let pseudonymized_message = pseudo_response
        .get("text")
        .and_then(|v| v.as_str())
        .unwrap_or(&request.message)
        .to_string();

    // Build a modified request with the pseudonymized message
    let mut sanitized_request = request.clone();
    sanitized_request.message = pseudonymized_message;

    // Also pseudonymize history messages
    // TODO: Batch pseudonymization for history messages for better performance
    let mut sanitized_history = Vec::with_capacity(request.history.len());
    for msg in &request.history {
        if msg.role == "user" {
            let hist_payload = serde_json::json!({
                "action": "pseudonymize",
                "text": msg.content,
                "sigilo_level": request.sigilo_level,
            });
            let hist_response = sidecar_guard
                .call("pseudonymize", hist_payload)
                .await
                .map_err(|e| AppError::Sidecar(format!("History pseudonymization failed: {e}")))?;
            let sanitized_content = hist_response
                .get("text")
                .and_then(|v| v.as_str())
                .unwrap_or(&msg.content)
                .to_string();
            sanitized_history.push(ChatMessageDto {
                role: msg.role.clone(),
                content: sanitized_content,
            });
        } else {
            sanitized_history.push(msg.clone());
        }
    }
    sanitized_request.history = sanitized_history;

    // -----------------------------------------------------------------------
    // Step 2: Send to LLM via sidecar (streaming)
    // -----------------------------------------------------------------------
    let llm_payload = build_sidecar_payload(&sanitized_request);

    // The sidecar returns a stream of JSON lines (NDJSON).
    // Each line is a ChatStreamChunk-like object.
    let stream_receiver = sidecar_guard
        .call_stream("llm_chat", llm_payload)
        .await
        .map_err(|e| AppError::Sidecar(format!("LLM stream initiation failed: {e}")))?;

    // -----------------------------------------------------------------------
    // Step 3: Forward chunks to the frontend via Tauri events
    // -----------------------------------------------------------------------
    let mut full_response = String::new();
    let mut total_tokens: u32 = 0;

    while let Ok(chunk_json) = stream_receiver.recv_async().await {
        let delta = chunk_json
            .get("delta")
            .and_then(|v| v.as_str())
            .unwrap_or("")
            .to_string();

        let done = chunk_json
            .get("done")
            .and_then(|v| v.as_bool())
            .unwrap_or(false);

        let token_count = chunk_json
            .get("token_count")
            .and_then(|v| v.as_u64())
            .map(|v| v as u32);

        if let Some(tc) = token_count {
            total_tokens = tc;
        }

        full_response.push_str(&delta);

        // If this is the final chunk, de-pseudonymize the full response
        let emit_delta = if done {
            // Step 4: De-pseudonymize the complete response
            let depseudo_payload = serde_json::json!({
                "action": "depseudonymize",
                "text": full_response,
                "sigilo_level": request.sigilo_level,
            });

            match sidecar_guard.call("depseudonymize", depseudo_payload).await {
                Ok(depseudo_response) => {
                    let depseudonymized = depseudo_response
                        .get("text")
                        .and_then(|v| v.as_str())
                        .unwrap_or(&full_response)
                        .to_string();
                    // For the final chunk, send the full de-pseudonymized text
                    // The frontend should replace the accumulated text with this
                    depseudonymized
                }
                Err(e) => {
                    // Log the error but still send the pseudonymized response
                    tracing::warn!(
                        conversation_id = %request.conversation_id,
                        error = %e,
                        "De-pseudonymization failed, returning pseudonymized response"
                    );
                    full_response.clone()
                }
            }
        } else {
            delta
        };

        let chunk = ChatStreamChunk {
            conversation_id: request.conversation_id.clone(),
            delta: emit_delta,
            done,
            token_count,
        };

        app.emit("ia:chat-stream", &chunk)
            .map_err(|e| AppError::Internal(format!("Failed to emit chat chunk: {e}")))?;

        if done {
            break;
        }
    }

    // -----------------------------------------------------------------------
    // Step 5: Audit log
    // -----------------------------------------------------------------------
    // TODO: Write audit entry to SQLCipher via infra::database
    // Should record: conversation_id, timestamp, user_id, token_count,
    // model used, sigilo_level. Must NOT store message content for sigilo >= 3.
    tracing::info!(
        conversation_id = %request.conversation_id,
        tokens = total_tokens,
        sigilo = request.sigilo_level,
        "Chat completion finished"
    );

    Ok(())
}

/// Cancel an in-progress chat stream.
#[command]
pub async fn ia_chat_cancel<R: Runtime>(
    app: AppHandle<R>,
    conversation_id: String,
) -> Result<(), String> {
    let sidecar = app
        .try_state::<Arc<Mutex<SidecarManager>>>()
        .ok_or("Sidecar manager not initialised")?;

    let sidecar_guard = sidecar.lock().await;

    sidecar_guard
        .call(
            "llm_cancel",
            serde_json::json!({ "conversation_id": conversation_id }),
        )
        .await
        .map_err(|e| format!("Failed to cancel stream: {e}"))?;

    // Emit a final "done" chunk so the frontend cleans up.
    let chunk = ChatStreamChunk {
        conversation_id,
        delta: String::new(),
        done: true,
        token_count: None,
    };
    app.emit("ia:chat-stream", &chunk)
        .map_err(|e| format!("Failed to emit cancel chunk: {e}"))?;

    Ok(())
}

/// Check the availability and status of the local AI backend.
#[command]
pub async fn ia_status<R: Runtime>(
    app: AppHandle<R>,
) -> Result<AiStatusResponse, String> {
    let sidecar = app
        .try_state::<Arc<Mutex<SidecarManager>>>()
        .ok_or("Sidecar manager not initialised")?;

    let sidecar_guard = sidecar.lock().await;

    let response = sidecar_guard
        .call("llm_status", serde_json::json!({}))
        .await
        .map_err(|e| format!("Failed to query AI status: {e}"))?;

    let available = response
        .get("available")
        .and_then(|v| v.as_bool())
        .unwrap_or(false);

    let backend = response
        .get("backend")
        .and_then(|v| v.as_str())
        .unwrap_or("none")
        .to_string();

    let models = response
        .get("models")
        .and_then(|v| v.as_array())
        .map(|arr| {
            arr.iter()
                .filter_map(|v| v.as_str().map(String::from))
                .collect()
        })
        .unwrap_or_default();

    Ok(AiStatusResponse {
        available,
        backend,
        models,
    })
}

/// List available local LLM models.
#[command]
pub async fn ia_list_models<R: Runtime>(
    app: AppHandle<R>,
) -> Result<Vec<String>, String> {
    let status = ia_status(app).await?;
    Ok(status.models)
}

