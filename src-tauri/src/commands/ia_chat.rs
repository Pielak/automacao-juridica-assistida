#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_build_sidecar_payload_defaults() {
        let request = ChatRequest {
            conversation_id: "test-123".into(),
            message: "Qual o prazo para recurso?".into(),
            history: vec![],
            sigilo_level: 1,
            model: None,
            system_prompt: None,
            temperature: None,
            max_tokens: None,
        };

        let payload = build_sidecar_payload(&request);

        let messages = payload["messages"].as_array().unwrap();
        assert_eq!(messages.len(), 2); // system + user
        assert_eq!(messages[0]["role"], "system");
        assert_eq!(messages[1]["role"], "user");
        assert_eq!(messages[1]["content"], "Qual o prazo para recurso?");
        assert_eq!(payload["stream"], true);
        assert!(payload.get("model").is_none());
    }

    #[test]
    fn test_build_sidecar_payload_with_overrides() {
        let request = ChatRequest {
            conversation_id: "test-456".into(),
            message: "Analise este contrato.".into(),
            history: vec![
                ChatMessageDto {
                    role: "user".into(),
                    content: "Olá".into(),
                },
                ChatMessageDto {
                    role: "assistant".into(),
                    content: "Olá! Como posso ajudar?".into(),
                },
            ],
            sigilo_level: 3,
            model: Some("llama3".into()),
            system_prompt: Some("Você é um especialista em contratos.".into()),
            temperature: Some(0.3),
            max_tokens: Some(2048),
        };

        let payload = build_sidecar_payload(&request);

        let messages = payload["messages"].as_array().unwrap();
        // system + 2 history + current user = 4
        assert_eq!(messages.len(), 4);
        assert_eq!(messages[0]["content"], "Você é um especialista em contratos.");
        assert_eq!(payload["model"], "llama3");
        assert_eq!(payload["temperature"], 0.3);
        assert_eq!(payload["max_tokens"], 2048);
    }

    #[test]
    fn test_chat_stream_chunk_serialization() {
        let chunk = ChatStreamChunk {
            conversation_id: "conv-1".into(),
            delta: "Olá".into(),
            done: false,
            token_count: Some(5),
        };

        let json = serde_json::to_value(&chunk).unwrap();
        assert_eq!(json["conversationId"], "conv-1");
        assert_eq!(json["delta"], "Olá");
        assert_eq!(json["done"], false);
        assert_eq!(json["tokenCount"], 5);
    }
}
